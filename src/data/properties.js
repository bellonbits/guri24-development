export const getPropertyBySlug = (slug) => {
  return properties.find(p => p.slug === slug);
};

export const getPropertiesByPurpose = (purpose) => {
  if (!purpose || purpose === 'all') return properties;
  return properties.filter(p => p.purpose.toLowerCase() === purpose.toLowerCase());
};

export const getPropertiesByType = (type) => {
  if (!type || type === 'all') return properties;
  return properties.filter(p => p.type.toLowerCase() === type.toLowerCase());
};

export const formatPrice = (price, currency, priceUnit) => {
  const formatted = price.toLocaleString();
  return `${currency}${formatted}${priceUnit ? ` ${priceUnit}` : ''}`;
};
